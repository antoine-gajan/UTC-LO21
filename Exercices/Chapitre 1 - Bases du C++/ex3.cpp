//Définition, initialisation et affectation de variables

#include <iostream>
using namespace std;

int main()
{
	double x = 3.14;
	cout<<"La valeur de x est : "<<x<<"\n";
	double y;
	cout<<"La valeur de y avant affectation est : "<<y<<"\n";
	y = 3.14;
	cout<<"La valeur de y après affectation est : "<<y<<"\n";
	return 0;
}